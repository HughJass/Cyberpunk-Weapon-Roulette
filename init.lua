function activeWeap()
    cw = es:GetActiveItem(player, "Weapon")
    wd = ts:GetItemData(player, cw)
    oldWeap = wd:GetID()
    return(oldWeap)
end

function removeWeap()
    local currentWeapon = espd:GetActiveWeapon()
    ts:RemoveItem(player, currentWeapon, 1)
    espd:ClearAllWeaponSlots()
end

function giveWeap()
    espd:ClearAllWeaponSlots()
    r = math.random(#gunstr)
    w = gunstr[r]
    Game.EquipItemToHand(w)
end

function upgradeWeapon()
    local player = Game.GetPlayer()
    local ssc = Game.GetScriptableSystemsContainer()
    local ts = Game.GetTransactionSystem()
    local ss = Game.GetStatsSystem()
    local es = ssc:Get(CName.new('EquipmentSystem'))

    local espd = es:GetPlayerData(player)
    espd['GetItemInEquipSlot2'] = espd['GetItemInEquipSlot;gamedataEquipmentAreaInt32']
    local playerLValue = ss:GetStatValue(player:GetEntityID(), 'Level')
    local playerPLValue = ss:GetStatValue(player:GetEntityID(), 'PowerLevel')

    local slots = {
        Weapon = 3
    }

    for k,v in pairs(slots) do
        for i=1,v do
            local itemid = espd:GetItemInEquipSlot2(k, i - 1)
            if itemid.tdbid.hash ~= 0 then 
                itemdata = ts:GetItemData(player, itemid)
                local statObj = itemdata:GetStatsObjectID()
                ss:RemoveAllModifiers(statObj, 'ItemLevel', true)
                ss:RemoveAllModifiers(statObj, 'CritChance', true)
                ss:RemoveAllModifiers(statObj, 'CritDamage', true)
                ss:RemoveAllModifiers(statObj, 'HeadshotDamageMultiplier', true)
                local statPLevel = Game['gameRPGManager::CreateStatModifier;gamedataStatTypegameStatModifierTypeFloat']('ItemLevel', 'Additive', playerPLValue * 15)
                local statCritChance = Game['gameRPGManager::CreateStatModifier;gamedataStatTypegameStatModifierTypeFloat']('CritChance', 'Additive', 50 * (playerLValue / 50))
                local statCritDamage = Game['gameRPGManager::CreateStatModifier;gamedataStatTypegameStatModifierTypeFloat']('CritDamage', 'Additive', 100 * (playerLValue / 50))
                local statHeadshot = Game['gameRPGManager::CreateStatModifier;gamedataStatTypegameStatModifierTypeFloat']('HeadshotDamageMultiplier', 'Additive', 2.5 * (playerLValue / 50))
                ss:AddSavedModifier(statObj, statPLevel)
                ss:AddSavedModifier(statObj, statCritChance)
                ss:AddSavedModifier(statObj, statCritDamage)
                ss:AddSavedModifier(statObj, statHeadshot)

                if rarityModifier == "Random" then
                    local r = math.random(#randomList)
                    rarityModifier = randomList[r]
                        if rarityModifier == "Legendary" then
                            Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Legendary'))
                            rarityModifier = "Random"
                        end
                        if rarityModifier == "Epic" then
                            Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Epic'))
                            rarityModifier = "Random"
                        end
                        if rarityModifier == "Rare" then
                            Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Rare'))
                            rarityModifier = "Random"
                        end
                        if rarityModifier == "Uncommon" then
                            Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Uncommon'))
                            rarityModifier = "Random"
                        end
                        if rarityModifier == "Common" then
                            Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Common'))
                            rarityModifier = "Random"
                        end
                end

                if rarityModifier == "Legendary" then
                    Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Legendary'))
                end
                if rarityModifier == "Epic" then
                    Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Epic'))
                end
                if rarityModifier == "Rare" then
                    Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Rare'))
                end
                if rarityModifier == "Uncommon" then
                    Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Uncommon'))
                end
                if rarityModifier == "Common" then
                    Game['gameRPGManager::ForceItemQuality;GameObjectgameItemDataCName'](player, itemdata, CName.new('Common'))
                end
            end
        end
    end
end

function removeQuest()
    local player = Game.GetPlayer()
    local ssc = Game.GetScriptableSystemsContainer()
    local ts = Game.GetTransactionSystem()
    local es = ssc:Get(CName.new('EquipmentSystem'))
    local espd = es:GetPlayerData(player)
    local currentWeapon = espd:GetActiveWeapon()
    local itemdata = ts:GetItemData(player, currentWeapon)
    
    if itemdata:HasTag("Quest") then
        itemdata:RemoveDynamicTag("Quest")
    end
end

registerForEvent("onUpdate", function (timeDelta)
    
    -- Combat status check
    if initiatedMod == true then
        startCombat = player:IsInCombat()
        isDead = player:IsDead()
    end

    if initiatedMod == true and isDead then
    	initiatedMod = false
    	startMod = false
    	startTimer = false
    end

    if firstGunSpawned == true then
        upgradeWeapon()
        removeQuest()
        firstGunSpawned = false
    end

    if initiatedMod == true and startMod == false then
        upgradeWeapon()
        startMod = true
    end

    -- Stop the timer if leaving combat
    if initiatedMod == true and startCombat == false then
        startTimer = false
    end

    -- Entering combat setting / reseting the start time of the weapon spawning cycle
    if initiatedMod == true and startCombat == true and startTimer == false then
        startTime = os.time()
        qa_tracker = os.time()
        startTimer = true
    end

    -- Updating a second timer to compare against the start time
    if initiatedMod == true and startCombat == true and startTimer == true then
        Time = os.time()
        timeElapsed = interval - (Time - startTime)
    end

    -- Removing weapon when the cycle is finished and still in combat
    if initiatedMod == true and timeElapsed == 0 and removedWeapon == false and startCombat == true then
        removeWeap()
        startTime = os.time()
        removedWeapon = true
    end

    -- Giving gun once the gun removing section tags the old weapon as nil and resetting the timer
    if initiatedMod == true and startCombat == true and removedWeapon == true then
        -- oldWeap = activeWeap()
        giveWeap()
        gaveWeapon = true
        removedWeapon = false
    end

    -- Giving weapon triggers gaveWeapon, signaling to upgrade active weapon
    if initiatedMod == true and startCombat == true and gaveWeapon == true then
        removeQuest()
        upgradeWeapon()
        Game.AddToInventory("Ammo.RifleAmmo",700);
        Game.AddToInventory("Ammo.ShotgunAmmo",100);
        Game.AddToInventory("Ammo.SniperRifleAmmo",100);
        Game.AddToInventory("Ammo.HandgunAmmo",500);
        gaveWeapon = false
    end

    -- Ignore
    if initiatedMod == true and qa_tracker == (Time + 1) then
        print("OldWeap: " .. tostring(oldWeap))
        qa_tracker = qa_tracker + 1
    end
end)

